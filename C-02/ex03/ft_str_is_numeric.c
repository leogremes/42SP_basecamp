int	ft_str_is_numeric(char *str)
{
	int		i;
	int		flag;

	i = 0;
	flag = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 48 || str[i] > 57)
		{
			flag = 0;
		}
		i++;
	}
	return (flag);
}
