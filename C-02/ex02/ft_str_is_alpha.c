int	ft_str_is_alpha(char *str)
{
	int		i;
	int		flag;
	char	c;

	i = 0;
	flag = 1;
	while (str[i] != '\0')
	{
		c = str[i];
		if (! ((65 <= c && c <= 90) || (97 <= c && c <= 122)))
		{
			flag = 0;
		}
		i++;
	}
	return (flag);
}
